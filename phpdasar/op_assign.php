<?php
$angka = 5;
echo('angka awal ='.$angka.'<br>');
$angka += 3; // $angka sekarang bernilai 8 (5 + 3)
echo('angka awal ditambahkan 3 ='.$angka.'<br>');
$angka -= 2; // $angka sekarang bernilai 6 (8 - 2)
echo('angka kedua dikurangi 2 ='.$angka.'<br>');
$angka *= 4; // $angka sekarang bernilai 24 (6 * 4)
echo('angka ketiga dikalikan 4 ='.$angka.'<br>');
$angka /= 3; // $angka sekarang bernilai 8 (24 / 3)
echo('angka keempat dibagi 3 ='.$angka.'<br>');
$angka %= 5; // $angka sekarang bernilai 3 (8 % 5)
echo('angka kelima sisa hasil bagi 5 ='.$angka.'<br>');
?>